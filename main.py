class StockDataFetcher:
    def __init__(self):
        self.handler = HFStreamingHandler(token=HF_TOKEN) if HF_TOKEN else None
        self.all_symbols = None

    async def get_all_symbols(self):
        """ডাটাসেটের সব সিম্বল লিস্ট করুন"""
        if self.all_symbols is not None:
            return self.all_symbols
        
        try:
            if not self.handler:
                logger.error("HFStreamingHandler ইনিশিয়ালাইজ হয়নি")
                return []
            
            symbols = self.handler.get_symbol_list(max_symbols=500)
            if symbols:
                self.all_symbols = symbols
                logger.info(f"মোট {len(self.all_symbols)}টি সিম্বল পাওয়া গেছে")
            else:
                self.all_symbols = []
            
            return self.all_symbols
                
        except Exception as e:
            logger.error(f"সিম্বল লিস্ট পেতে সমস্যা: {e}")
            return []

    async def get_stock_data(self, symbol: str, rows=400):
        """হাগিং ফেস থেকে সম্পূর্ণ ডাটা সংগ্রহ করুন"""
        try:
            logger.info(f"{symbol} এর জন্য ডাটা সংগ্রহ করা হচ্ছে...")

            if not self.handler:
                logger.error("HFStreamingHandler সেট করা নেই!")
                return None, 0, []

            df = self.handler.get_symbol_latest_rows(symbol, rows=rows)
            
            if df is None or df.empty:
                similar_symbols = self.handler.search_symbols(symbol, limit=15)
                logger.info(f"{symbol} এর জন্য সিমিলার সিম্বল: {similar_symbols}")
                return None, 0, similar_symbols
            
            rows_available = len(df)
            logger.info(f"{symbol} এর জন্য {rows_available}টি রো পাওয়া গেছে")
            
            # date কলাম থাকলে সাজান
            date_col = self.handler._find_date_column(df) if self.handler else None
            if date_col and date_col in df.columns:
                df[date_col] = pd.to_datetime(df[date_col], errors='coerce')
                df = df.sort_values(date_col, ascending=True)
            
            return df, rows_available, []

        except Exception as e:
            logger.error(f"ডাটা ফেচিং এ সমস্যা: {traceback.format_exc()}")
            return None, 0, []

