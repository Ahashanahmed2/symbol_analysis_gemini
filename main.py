    content = file_data['content']
    filename = file_data['filename']
    
    file_stream = io.BytesIO(content.encode('utf-8'))
    file_stream.seek(0)
    
    return send_file(
        file_stream,
        as_attachment=True,
        download_name=filename,
        mimetype='text/plain'
    )

